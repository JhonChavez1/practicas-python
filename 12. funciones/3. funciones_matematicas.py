# math es una libreria que contiene funciones matemáticas

import math

# pow: eleva un número a un exponente
print(math.pow(10, 2))
# sqrt: saca la raíz cuadrada de un número
print(math.sqrt(64))
# para sacar la raíz cuadrada de un número pero en flotante
print(math.sqrt(64))
# sacar el seno
print(math.sin(90))
# sacar el coseno
print(math.cos(90))
# sacar el tangente
print(math.tan(90))
# sacar el factorial
print(math.factorial(5))
