# crear un programa que tenga la variable: "Te quiero solo como amigo y muestre la siguiente información"

texto = "Te quiero solo como amigo"

# Imprima los 2 primeros caracteres
print(texto[0: 2])

# imprima los 3 últimos caracteres
print(texto[-3: ])

# imprima el texto cada 2 caracteres
print(texto[: : 2])

# imprimir texto en cadena inversa
print(texto[: : -1])

# imprimer el texto en ambos sentidos
print(texto + texto[: : -1])